CREATE TABLE flights (
    id_vuelo INT PRIMARY KEY,
    numero_vuelo VARCHAR(20),
    aerolinea VARCHAR(100),
    origen VARCHAR(100),
    destino VARCHAR(100),
    fecha_salida DATE,
    hora_salida TIME,
    estado VARCHAR(20),
    puerta_embarque VARCHAR(10)
);

CREATE TABLE personal (
    id_personal INT PRIMARY KEY,
    nombre VARCHAR(100),
    apellido VARCHAR(100),
    puesto VARCHAR(100),
    departamento VARCHAR(100)
);

CREATE TABLE passengers (
    id_pasajero INT PRIMARY KEY,
    nombre VARCHAR(100),
    apellido VARCHAR(100),
    fecha_nacimiento DATE,
    documento_identidad VARCHAR(20)
);

CREATE TABLE baggage (
    id_equipaje INT PRIMARY KEY,
    id_pasajero INT,
    numero_vuelo INT,
    estado VARCHAR(20),
    CONSTRAINT fk_pasajero FOREIGN KEY (id_pasajero) REFERENCES passengers(id_pasajero),
    CONSTRAINT fk_vuelo FOREIGN KEY (numero_vuelo) REFERENCES flights(id_vuelo)
);

CREATE TABLE airportServices (
    id_servicio INT PRIMARY KEY,
    nombre VARCHAR(100),
    tipo VARCHAR(50),
    ubicacion VARCHAR(100)
);

CREATE TABLE logins (
    id INT PRIMARY KEY AUTO_INCREMENT,
    usuario VARCHAR(50) NOT NULL UNIQUE,
    contraseña VARCHAR(255) NOT NULL
);


